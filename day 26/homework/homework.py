# [1, 2, 3, 4] -> [2, 1, 6, 2]
# [5, 10, 3, 5] -> [10, 5, 6, 10]


def transform_list(nums):
    new_list = []
    for num in nums:
        if num % 2 == 0:
            new_list.append(num / 2)
        else:
            new_list.append(num * 2)
    return new_list